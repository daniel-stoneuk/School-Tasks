package com.danielstone.shapes;

import javafx.scene.paint.Color;

/**
 * Created by user on 25/02/2016.
 */
public class Circle {

    int x;
    int y;

    int height;
    int width;

    Color color;

    public Circle(int x, int y, int height, int width, Color color) {
        this.x = x;
        this.y = y;
        this.height = height;
        this.width = width;
        this.color = color;
    }

}
